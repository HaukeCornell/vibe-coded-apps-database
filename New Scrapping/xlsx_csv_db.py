from google.colab import files
import pandas as pd
import sqlite3
import os

# --- Step 1: Upload all Excel files ---
print("ğŸ“¤ Please upload one or more Excel (.xlsx) files")
uploaded = files.upload()

# --- Step 2: Loop through each uploaded Excel file ---
for excel_file in uploaded.keys():
    base_name = os.path.splitext(excel_file)[0]
    csv_file = f"{base_name}.csv"
    db_file = f"{base_name}.db"
    table_name = base_name

    print(f"\nğŸ“„ Processing {excel_file} ...")

    # --- Convert Excel â†’ CSV ---
    try:
        df = pd.read_excel(excel_file)
        df.to_csv(csv_file, index=False)
        print(f"âœ… Converted to {csv_file}")
    except Exception as e:
        print(f"âŒ Error reading {excel_file}: {e}")
        continue

    # --- Create SQLite DB and insert data ---
    try:
        conn = sqlite3.connect(db_file)
        df.to_sql(table_name, conn, if_exists='replace', index=False)
        conn.close()
        print(f"ğŸ—ƒï¸ Created database {db_file} with table '{table_name}'")
    except Exception as e:
        print(f"âŒ Error creating DB for {excel_file}: {e}")
        continue

# --- Step 3: Offer downloads for all generated files ---
print("\nâ¬‡ï¸ Download your generated CSV and DB files:")
for excel_file in uploaded.keys():
    base_name = os.path.splitext(excel_file)[0]
    csv_file = f"{base_name}.csv"
    db_file = f"{base_name}.db"
    try:
        files.download(csv_file)
        files.download(db_file)
    except Exception as e:
        print(f"âš ï¸ Could not auto-download {base_name}: {e}")

print("\nâœ… All conversions complete!")

